  def self.signs
    SIGNS
  end
  
  def self.str_to_date(date_string)
    Date.new(*ParseDate.parsedate(date_string)[0..2]) rescue nil
  end
  
  def self.sign_from_date(month, day)
    DATES_TO_SUN_SIGNS[[month, day]]
  end
  
  def self.element_from_sign(sign)
    ELEMENTS_TO_SIGNS.each do |element, sign_array|
      return element if sign_array.include?(sign)
    end
    nil
  end
  
  def self.compute_midpoint(date1, date2)
    this_year = date1.year
    d1 = Date.new(this_year, date1.month, date1.day)
    d2 = Date.new(this_year, date2.month, date2.day)
    days_to_midpoint = ((d1 - d2) / 2.0).abs
    (d1 > d2) ? (d2 + days_to_midpoint) : (d1 + days_to_midpoint)
  end
  
  def self.joint_status(birthdate1, birthdate2, date = Date.today)
    element1 = element_from_sign(sign_from_date(birthdate1.month, birthdate1.day))
    element2 = element_from_sign(sign_from_date(birthdate2.month, birthdate2.day))
    moon_element = element_from_sign(MOON_SIGNS[[date.year, date.month, date.day]][0])
    SUN_SUN_MOON_TO_FRIEND_STATUS[[element1, element2, moon_element]]
  end
  
  def self.joint_status_by_sign(sun_sign1, sun_sign2, date = Date.today)
    element1 = element_from_sign(sun_sign1)
    element2 = element_from_sign(sun_sign2)
    moon_element = element_from_sign(MOON_SIGNS[[date.year, date.month, date.day]][0])
    SUN_SUN_MOON_TO_FRIEND_STATUS[[element1, element2, moon_element]]
  end
  
  def self.friendscope_content(birthdate1, birthdate2, date = Date.today)
    midpoint = compute_midpoint(birthdate1, birthdate2)
    joint_sun_sign = sign_from_date(midpoint.month, midpoint.day)
    moon_sign, moon_day_number = MOON_SIGNS[[date.year, date.month, date.day]]
    FRIENDSCOPE_CONTENT[[joint_sun_sign, moon_sign, moon_day_number]]
  end
  
